package com.eversis.esa.geoss.contents.exception;

/**
 * The type File name not unique exception.
 */
public class FileNameNotUniqueException extends RuntimeException {

    /**
     * Instantiates a new File name not unique exception.
     */
    public FileNameNotUniqueException() {
        super();
    }

    /**
     * Instantiates a new File name not unique exception.
     *
     * @param message the message
     */
    public FileNameNotUniqueException(String message) {
        super(message);
    }

}
