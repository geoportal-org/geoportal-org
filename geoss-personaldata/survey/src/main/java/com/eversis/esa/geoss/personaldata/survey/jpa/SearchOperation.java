package com.eversis.esa.geoss.personaldata.survey.jpa;

/**
 * The enum Search operation.
 */
public enum SearchOperation {
    /**
     * Greater than search operation.
     */
    GREATER_THAN,
    /**
     * Less than search operation.
     */
    LESS_THAN,
    /**
     * Greater than equal search operation.
     */
    GREATER_THAN_EQUAL,
    /**
     * Less than equal search operation.
     */
    LESS_THAN_EQUAL,
    /**
     * Not equal search operation.
     */
    NOT_EQUAL,
    /**
     * Equal search operation.
     */
    EQUAL,
    /**
     * Like search operation.
     */
    LIKE,
    /**
     * Like start search operation.
     */
    LIKE_START,
    /**
     * Like end search operation.
     */
    LIKE_END,
    /**
     * In search operation.
     */
    IN,
    /**
     * Not in search operation.
     */
    NOT_IN
}
